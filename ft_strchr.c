char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i] != c)
	{
		i++;
		if (str[i] == '\0' && c != '\0')
			return (0);
	}
	return (str + i);
}
